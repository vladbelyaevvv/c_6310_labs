#include "matrix.h"

// Проверяет симметричность матрицы: A[i][j] == A[j][i] для всех i, j.
// Симметричной может быть только квадратная матрица.
// Возвращает 1 если симметрична, 0 если нет.
int	is_symmetric(matrix_t *A)
{
	// Не квадратная матрица не может быть симметричной
	if (!A || A->rows != A->columns)
		return (0);

	for(int i = 0; i < A->rows; i++)
	{
		for(int j = 0; j < A->columns; j++)
		{
			// Сравниваем элемент с его отражением относительно диагонали
			if (A->matrix[i][j] != A->matrix[j][i])
				return (0);
		}
	}
	return (1);
}
